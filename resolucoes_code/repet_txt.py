# Solicita ao usuário que insira um texto
texto = input("Digite o texto a ser repetido: ")

# Solicita ao usuário que insira o número de repetições
repeticoes = int(input("Digite o número de repetições: "))

# Repete o texto o número especificado de vezes
resultado = texto * repeticoes

# Exibe o resultado
print("Texto repetido:", resultado)